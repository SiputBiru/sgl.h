// LOGS
static void
sgl_LogOutputFunction(void* userdata, int category, SDL_LogPriority priority, const char* message) {
	const char* color = "\033[0m";
	const char* reset = "\033[0m";
	const char* tag = "[INFO]";
	FILE* stream = stdout;

	switch (priority) {
	case SDL_LOG_PRIORITY_VERBOSE:
		tag = "[VERB]";
		color = "\033[90m";
		break; // Gray
	case SDL_LOG_PRIORITY_DEBUG:
		tag = "[DBUG]";
		color = "\033[36m";
		break; // Cyan
	case SDL_LOG_PRIORITY_INFO:
		tag = "[INFO]";
		color = "\033[32m";
		break; // Green
	case SDL_LOG_PRIORITY_WARN:
		tag = "[WARN]";
		color = "\033[33m";
		break; // Yellow
	case SDL_LOG_PRIORITY_ERROR:
		tag = "[ERR ]";
		color = "\033[31m";
		stream = stderr;
		break; // Red
	case SDL_LOG_PRIORITY_CRITICAL:
		tag = "[CRIT]";
		color = "\033[41;37m";
		stream = stderr;
		break; // White on Red Bg
	default:
		break;
	}

	// Format: [TAG] SGL: Message
	if (category == SGL_LOG_CATEGORY) {
		fprintf(stream, "%s%s SGL: %s%s\n", color, tag, message, reset);
	} else {
		// If other parts of SDL log something, print it cleanly too
		fprintf(stream, "%s%s %s%s\n", color, tag, message, reset);
	}
}

static void sgl_SetViewport(SDL_GPURenderPass* pass) {
	int physW, physH;
	SDL_GetWindowSizeInPixels(sgl.window, &physW, &physH);

	SDL_GPUViewport viewport = {
		.x = 0,
		.y = 0,
		.w = (f32)physW,
		.h = (f32)physH,
		.min_depth = 0.0f,
		.max_depth = 1.0f,
	};
	SDL_SetGPUViewport(pass, &viewport);

	SDL_Rect scissor = { 0, 0, physW, physH };
	SDL_SetGPUScissor(pass, &scissor);
}

static void sgl_CheckResize(void) {
	int w, h;
	SDL_GetWindowSizeInPixels(sgl.window, &w, &h);

	if (sgl.depthTexture == NULL || w != sgl.winW || h != sgl.winH) {
		sgl.winW = w;
		sgl.winH = h;

		if (sgl.depthTexture) {
			SDL_ReleaseGPUTexture(sgl.device, sgl.depthTexture);
		}

		SDL_GPUTextureCreateInfo depthInfo = {
			.type = SDL_GPU_TEXTURETYPE_2D,
			.format = SDL_GPU_TEXTUREFORMAT_D16_UNORM,
			.width = (uint32_t)w,
			.height = (uint32_t)h,
			.layer_count_or_depth = 1,
			.num_levels = 1,
			.usage = SDL_GPU_TEXTUREUSAGE_DEPTH_STENCIL_TARGET,
		};
		sgl.depthTexture = SDL_CreateGPUTexture(sgl.device, &depthInfo);
	}
}

